export const DEFAULT_UI_LANG: UiLang = "en";

// Detect from navigator.language. Falls back to English for unsupported codes.
export function detectBrowserLang(navigatorLang: string | undefined): UiLang {
  if (!navigatorLang) return DEFAULT_UI_LANG;
  const base = navigatorLang.toLowerCase().split("-")[0];
  if ((SUPPORTED_UI_LANGUAGES as readonly string[]).includes(base)) return base as UiLang;
  return DEFAULT_UI_LANG;
}

