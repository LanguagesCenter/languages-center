export const translations: Record<UiLang, Dict> = {
  en, es, fr, de, el, sv, da, fi, sq, is,
};

// Translate a key under the given language.
// Falls back to English, then to the raw key. Supports {variable} interpolation.
export function translate(
  lang: UiLang,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const fromLang = translations[lang]?.[key];
  const value = fromLang ?? translations.en[key] ?? key;
  if (!vars) return value;
  return value.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? `{${name}}`));
}
