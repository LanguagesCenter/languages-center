// Robust browser detection: prefers navigator.languages[] (ordered by user
// preference) over the single navigator.language. Splits codes like en-US,
// zh-Hans-CN, pt_BR cleanly and accepts the first base code we recognise.
export function detectBrowserLang(nav: {
  language?: string;
  languages?: readonly string[];
}): UiLang {
  const candidates = (nav.languages && nav.languages.length > 0
    ? nav.languages
    : nav.language
      ? [nav.language]
      : []) as readonly string[];

  for (const candidate of candidates) {
    if (!candidate) continue;
    const base = candidate.toLowerCase().split(/[-_]/)[0];
    if ((SUPPORTED_UI_LANGUAGES as readonly string[]).includes(base)) {
      return base as UiLang;
    }
  }
  return DEFAULT_UI_LANG;
}

// Capitalize first character honouring locale rules.
export function capitalizeFirst(value: string, locale?: string): string {
  if (!value) return value;
  return value.charAt(0).toLocaleUpperCase(locale) + value.slice(1);
}

// Localized language name for one of our slugs.
// Falls back to the provided English fallback if Intl.DisplayNames is
// unavailable or the lookup fails.
export function getLocalizedLanguageName(
  slug: string,
  uiLang: UiLang,
  fallback: string,
): string {
  if (typeof Intl === "undefined" || typeof Intl.DisplayNames === "undefined") {
    return fallback;
  }
  const iso = SLUG_TO_ISO[slug] ?? slug;
  try {
    const dn = new Intl.DisplayNames([uiLang], { type: "language" });
    const result = dn.of(iso);
    if (!result) return fallback;
    return capitalizeFirst(result, uiLang);
  } catch {
    return fallback;
  }
}

